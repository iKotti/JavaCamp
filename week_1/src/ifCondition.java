public class ifCondition {
    public static void main(String[] args) {
        int number = 10;

        if (number < 10) {
            System.out.println("Sayı 10'dan küçük.");
        } else if (number > 10) {
            System.out.println("Sayı 10'dan büyük.");
        } else {
            System.out.println("Sayı 10'a eşit.");
        }
    }
}
